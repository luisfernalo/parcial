
public class Persona {
	public String nombre;
	public String apellido;
	public String tipo;
	public String numero; 
	public String edad; 
	public String peso;
	public String altura;
	public String celular;
	public String direccion; 
	public String puesto;
	public Persona(String _nombre, String _apellido, String _tipo, String _numero, String _edad, String _peso,
			String _altura, String _celular, String _direccion, String _puesto) {

		 nombre=_nombre;
		 apellido=_apellido;
		 tipo=_tipo;
		 numero=_numero; 
		 edad=_edad; 
		 peso=_peso;
		 altura=_altura;
		 celular=_celular;
		 direccion=_direccion; 
		 puesto=_puesto;
	}

}
